    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = String.valueOf(txtPassword.getPassword());

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
            return;
        }

        LoginRepository loginRepository = new LoginRepository();
        List<Users> userList = loginRepository.LoginCheck();

        boolean loginSuccessful = false;
        Users loggedInUser = null;
        for (Users user : userList) {
            if (user.getUserName().equals(username) && user.getPassWord().equals(password)) {
                loginSuccessful = true;
                loggedInUser = user;
                break;

            }
        }

        if (loginSuccessful) {

            String displayName = loggedInUser.getDisplayName();

            JOptionPane.showMessageDialog(this, "Đăng nhập thành công. Xin chào, " + displayName);
            this.dispose();
            SearchBook.main(username);
        } else {

            JOptionPane.showMessageDialog(this, "Đăng nhập thất bại. Vui lòng kiểm tra lại tên người dùng và mật khẩu");
        }


    }//GEN-LAST:event_btnLoginActionPerformed

